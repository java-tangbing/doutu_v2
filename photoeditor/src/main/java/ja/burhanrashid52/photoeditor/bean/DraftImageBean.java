package ja.burhanrashid52.photoeditor.bean;

public class DraftImageBean {

    private String stickerImagePath;
    private float imageWidth;
    private float imageHeight;
    private float translationX;
    private float translationY;
    private float scaleX;
    private float scaleY;
    private float rotation;
    private float bgWidth;
    private float bgHeight;

    public float getBgWidth() {
        return bgWidth;
    }

    public void setBgWidth(float bgWidth) {
        this.bgWidth = bgWidth;
    }

    public float getBgHeight() {
        return bgHeight;
    }

    public void setBgHeight(float bgHeight) {
        this.bgHeight = bgHeight;
    }

    public float getTranslationX() {
        return translationX;
    }

    public void setTranslationX(float translationX) {
        this.translationX = translationX;
    }

    public float getTranslationY() {
        return translationY;
    }

    public void setTranslationY(float translationY) {
        this.translationY = translationY;
    }

    public float getScaleX() {
        return scaleX;
    }

    public void setScaleX(float scaleX) {
        this.scaleX = scaleX;
    }

    public float getScaleY() {
        return scaleY;
    }

    public void setScaleY(float scaleY) {
        this.scaleY = scaleY;
    }

    public float getRotation() {
        return rotation;
    }

    public void setRotation(float rotation) {
        this.rotation = rotation;
    }

    public String getStickerImagePath() {
        return stickerImagePath;
    }

    public void setStickerImagePath(String stickerImagePath) {
        this.stickerImagePath = stickerImagePath;
    }

    public float getImageWidth() {
        return imageWidth;
    }

    public void setImageWidth(float imageWidth) {
        this.imageWidth = imageWidth;
    }

    public float getImageHeight() {
        return imageHeight;
    }

    public void setImageHeight(float imageHeight) {
        this.imageHeight = imageHeight;
    }

}
